public class B02_Variables
{
    public static void main(String arg[]){
        
        int a = 4;
        float b = 4.56f;
        double d = 6.44423;
        char e = 'e';
        boolean c = true;

        System.out.println(a); // 4
        System.out.println(b); // 4.56
        System.out.println(d); // 6.44423
        System.out.println(e); // e
        System.out.println(c); // true
    }
}